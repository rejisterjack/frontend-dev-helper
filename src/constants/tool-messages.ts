export function getToolMessagePrefix(toolId: ToolId): string | undefined {
  return TOOL_MESSAGE_PREFIXES[toolId];
}

export function getToolMessageType(
  toolId: ToolId,
  action: 'ENABLE' | 'DISABLE' | 'TOGGLE'
): string | undefined {
  const prefix = TOOL_MESSAGE_PREFIXES[toolId];
  if (!prefix) return undefined;
  return `${prefix}_${action}`;
}
